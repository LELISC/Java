package filipe_lelis;

public class WhileContinue {

	public static void main(String[] args) {
		// while continue 
		
	int contadora = 0;
	
	while (contadora < 10 ) {
	
		contadora++;
		
		if (contadora == 5) {
			
			continue;
			
		}System.out.println(contadora);
		
		
	}
		
	

	}

}
