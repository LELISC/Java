package filipe_lelis;

public class WhileBreak {

	public static void main(String[] args) {
		// While Break
		
	int	contadora = 0;
		
		while (contadora < 20) {
			
			contadora++;
			
			if (contadora == 15 ) {
				
				break;
				
			}System.out.println(contadora);
		}

		
		
	}

}
